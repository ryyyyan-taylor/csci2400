/* 
 * bitAnd - x&y using only ~ and | 
 *   Example: bitAnd(6, 5) = 4
 *   Legal ops: ~ |
 *   Max ops: 8
 *   Rating: 1
 */
int bitAnd(int x, int y) {
    /* straight up Demorgan's Law */
    return ~(~x | ~y);
}
/* 
 * getByte - Extract byte n from word x
 *   Bytes numbered from 0 (LSB) to 3 (MSB)
 *   Examples: getByte(0x12345678,1) = 0x56
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 6
 *   Rating: 2
 */
int getByte(int x, int n) {
    /* push to certain value and mask others to get certain byte */
    return (x >> (n << 3)) & 0xFF;
}
/* 
 * logicalShift - shift x to the right by n, using a logical shift
 *   Can assume that 0 <= n <= 31
 *   Examples: logicalShift(0x87654321,4) = 0x08765432
 *   Legal ops: ~ & ^ | + << >>
 *   Max ops: 20
 *   Rating: 3 
 */
int logicalShift(int x, int n) {
    /* perform arithmetic shift then clear any bits to the left */
    int mask = (((x & (1 << 31)) >> n) << 1);

    return (x >> n) ^ mask;
}
/*
 * bitCount - returns count of number of 1's in word
 *   Examples: bitCount(5) = 2, bitCount(7) = 3
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 40
 *   Rating: 4
 */
int bitCount(int x) { 
    int temp, m1, m2, m3, m4, m5, total;
    
    /* 0x55555555 */
    temp = 0x55 | (0x55 << 8);
    m1 = temp | (temp << 16);
    
    /* 0x33333333 */
    temp = 0x33 | (0x33 << 8);
    m2 = temp | (temp << 16);
    
    /* 0x0F0F0F0F */
    temp = 0x0F | (0x0F << 8);
    m3 = temp | (temp << 16);
    
    /* 0x00FF00FF */
    m4 = 0xFF | (0xFF << 16);
    
    /* 0x0000FFFF */
    temp = 0xFF | (0xFF << 8);
    m5 = temp | (0 << 16);
    
    /* pass through each mask to get total */
    total = (x & m1) + ((x >> 1) & m1);
    total = (total & m2) + ((total >> 2) & m2);
    total = (total & m3) + ((total >> 4) & m3);
    total = (total & m4) + ((total >> 8) & m4);
    total = (total & m5) + ((total >> 16) & m5);
    
    return total;
}
/* 
 * bang - Compute !x without using !
 *   Examples: bang(3) = 0, bang(0) = 1
 *   Legal ops: ~ & ^ | + << >>
 *   Max ops: 12
 *   Rating: 4 
 */
int bang(int x) {
    /* negative value of x */
    int negX = ~x + 1;
    
    /* 0 is only value that neither x or -x are negative */
    int bit = (negX | x) >> 31;
    return bit + 1;
}
/* 
 * tmin - return minimum two's complement integer 
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 4
 *   Rating: 1
 */
int tmin(void) {
    /* 32 bit 2s complement, 0x80000000 is smallest number */
    return 1 << 31;
}
/* 
 * fitsBits - return 1 if x can be represented as an 
 *  n-bit, two's complement integer.
 *   1 <= n <= 32
 *   Examples: fitsBits(5,3) = 0, fitsBits(-4,3) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 2
 */
int fitsBits(int x, int n) {
    /* move 32-n to left then back to the right */
    int s = 33 + ~n;
    return !(x ^ ((x << s) >> s));
}
/* 
 * divpwr2 - Compute x/(2^n), for 0 <= n <= 30
 *  Round toward zero
 *   Examples: divpwr2(15,1) = 7, divpwr2(-33,4) = -2
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 2
 */
int divpwr2(int x, int n) {
    
    /* subtracts 1 from 2^n, accounts for need to possibly +1 */
    int mask = (1 << n) + ~0;
    int op = (x >> 31) & mask;
    
    /* adds 1 or 0 if originally negative or positive */
    return (x + op) >> n;
}
/* 
 * negate - return -x 
 *   Example: negate(1) = -1.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 5
 *   Rating: 2
 */
int negate(int x) {
    return (~x + 1);
}
/* 
 * isPositive - return 1 if x > 0, return 0 otherwise 
 *   Example: isPositive(-1) = 0.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 8
 *   Rating: 3
 */
int isPositive(int x) {
    /* xOR relation between bang of sign and bang of x */
    int c = !(x >> 31);
    return (!x) ^ c;
}
/* 
 * isLessOrEqual - if x <= y  then return 1, else return 0 
 *   Example: isLessOrEqual(4,5) = 1.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 24
 *   Rating: 3
 */
int isLessOrEqual(int x, int y) {
    /* returns 1 if signs are different on x and y */
    int sign = !(x >> 31) ^ !(y >> 31);
    /* if signs different and x < 0, z = 1 */
    int z = sign & (x >> 31);
    /* if signs same and diff is + or 0, v = 1 */
    int v = !sign & !((y + (~x + 1)) >> 31);
    
    return z | v;
}
